    public void authenticate() {
        Log.i(TAG, "authenticating...");
        BluetoothGattService gattService = bluetoothGatt.getService(EVG_SERVICE);
        BluetoothGattCharacteristic authChar = gattService.getCharacteristic(AUTH_CHAR);

        Log.i(TAG, "Using auth code: " + authcodes[authCounter]);
        authChar.setValue(stringToBytesASCII(authcodes[authCounter]));

        if (authCounter > authcodes.length) authCounter = 0;
        else authCounter++;

        boolean writeStatus = bluetoothGatt.writeCharacteristic(authChar);

        Log.i(TAG, "Write auth status: " + writeStatus);
    }





    public static byte[] stringToBytesASCII(String str) {
        byte[] b = new byte[str.length()];
        for (int i = 0; i < b.length; i++) {
            b[i] = (byte) str.charAt(i);
        }
        return b;
    }

